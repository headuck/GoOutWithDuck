package com.headuck.app.gooutwithduck.di

import android.content.Context
import com.headuck.app.gooutwithduck.data.*
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.android.components.ApplicationComponent
import dagger.hilt.android.qualifiers.ApplicationContext
import javax.inject.Singleton

@InstallIn(ApplicationComponent::class)
@Module
class DatabaseModule {

    @Singleton
    @Provides
    fun provideAppDatabase(@ApplicationContext context: Context): AppDatabase {
        return AppDatabase.getInstance(context)
    }

    @Provides
    fun provideVisitHistoryDao(appDatabase: AppDatabase): VisitHistoryDao {
        return appDatabase.visitHistoryDao()
    }

    @Provides
    fun provideDownloadCaseDao(appDatabase: AppDatabase): DownloadCaseDao {
        return appDatabase.downloadCaseDao()
    }

    @Provides
    fun provideInboxDao(appDatabase: AppDatabase): InboxDao {
        return appDatabase.inboxDao()
    }
}
